#include "stdafx.h"

#include "commit.h"

int mysql_commitx (
    mysql_context_def *context,
    int commit_action)
{
    MYSQL *mysql;
    int ret;

    if (-1 == context->mysql_current_connect)
        ret = RDBI_NOT_CONNECTED;
    else
    {
        mysql = context->mysql_connections[context->mysql_current_connect];
        if ((MYSQL*)NULL == mysql)
            ret = RDBI_NOT_CONNECTED;
        else
        {
            if (0 != mysql_commit (mysql))
                ret = RDBI_GENERIC_ERROR;
            else
                ret = RDBI_SUCCESS;
        }
    }

    return (ret);
}
