            function getTOCFileItems(tocItems)
            {
               var fItems = [];
               if(!tocItems) return fItems;
               for (var i = 0; i < tocItems.length; i++)
               {
                  if (typeof(tocItems[i]) == "object")
                  {
                     var childFItems = getTOCFileItems(tocItems[i]);
                     for (var j = 0; j < childFItems.length; j++)
                         fItems[fItems.length] = childFItems[j];
                  }
                  else if(i == 1) fItems[fItems.length] = tocItems[i];
               }
               return fItems;
            }
            